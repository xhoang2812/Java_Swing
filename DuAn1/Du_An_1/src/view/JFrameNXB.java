    private void btnThem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThem1ActionPerformed
        // TODO add your handling code here:
         for (NhaXuatBan ncc1 : data.getAll()) {
            if (ncc1.getTennxb().equalsIgnoreCase(txtten.getText().trim())) {
                JOptionPane.showMessageDialog(this, "Tên nhà xuất bản trùng, vui lòng nhập lại");
                return;
            }
        }
        NhaXuatBan nxb = checkAdd();
        if (nxb == null) {
            return;
        }
        data.add(nxb);
        JOptionPane.showMessageDialog(this, "Thêm thành công");
        btnSua2ActionPerformed(evt);
        filltable();

    }//GEN-LAST:event_btnThem1ActionPerformed

    private void btnSua1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSua1ActionPerformed
        // TODO add your handling code here:
        int row = tblnxb.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để sửa", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        this.checkUpdate(check());
        JOptionPane.showMessageDialog(this, "Sửa thành công");
        btnSua2ActionPerformed(evt);
        filltable();

    }//GEN-LAST:event_btnSua1ActionPerformed

    private void tblnxbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblnxbMouseClicked
        // TODO add your handling code here:
        int row = tblnxb.getSelectedRow();
        txtma.setText(tblnxb.getValueAt(row, 0).toString());
        txtten.setText(tblnxb.getValueAt(row, 1).toString());
        if (tblnxb.getValueAt(row, 2).toString() == "HOP TAC") {
            rdoHopTac.setSelected(true);
        } else {
            rdoNgungHopTac.setSelected(true);
        }
        if (tblnxb.getValueAt(row, 3) == null) {
            txtNgayTao.setText(null);
        } else {
            txtNgayTao.setText(tblnxb.getValueAt(row, 3).toString());
        }
        if (tblnxb.getValueAt(row, 4) == null) {
            txtNgaySua.setText(null);
        } else {
            txtNgaySua.setText(tblnxb.getValueAt(row, 4).toString());
        }

    }//GEN-LAST:event_tblnxbMouseClicked

