    private void filltable() {
        DefaultTableModel mol = (DefaultTableModel) this.tblnxb.getModel();
        mol.setRowCount(0);
        for (NhaXuatBan nxb : data.getAll()) {
            Object[] rowData = {
                nxb.getManxb(),
                nxb.getTennxb(),
                nxb.isTrangThai() == true ? "HOP TAC" : "NGUNG HOP TAC",
                nxb.getNgayTao(),
                nxb.getNgaySua()
            };
            mol.addRow(rowData);
        }

    }

    private void filltableTim() {
        DefaultTableModel molTk = (DefaultTableModel) this.tblnxb.getModel();
        molTk.setRowCount(0);
        for (NhaXuatBan nxb : data.getAllByName(txtTimKiem.getText().trim())) {
            Object[] rowData = {
                nxb.getManxb(),
                nxb.getTennxb(),
                nxb.isTrangThai() == true ? "HOP TAC" : "NGUNG HOP TAC",
                nxb.getNgayTao(),
                nxb.getNgaySua()
            };
            molTk.addRow(rowData);
        }

    }

    private NhaXuatBan check() {
        String maNXB = txtma.getText();
        String tenNXB = txtten.getText();
        boolean trangThai;
        if (rdoHopTac.isSelected()) {
            trangThai = true;
        } else {
            trangThai = false;
        }

        if (tenNXB.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return null;
        }
        NhaXuatBan nxb = new NhaXuatBan(Integer.parseInt(maNXB), tenNXB, trangThai);
        return nxb;
    }

    private NhaXuatBan checkAdd() {

        String tenNXB = txtten.getText();
        boolean trangThai;
        if (rdoHopTac.isSelected()) {
            trangThai = true;
        } else {
            trangThai = false;
        }
        if (tenNXB.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để sửa", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return null;
        }
        NhaXuatBan nxb = new NhaXuatBan(tenNXB, trangThai);
        return nxb;
    }

    private void checkUpdate(NhaXuatBan nxb) {
        data.update(nxb);
    }

